namespace Calculadora
{
    public class Calculadora
    {

        public int Soma(int v1, int v2)
        {
            return v1 + v2;
        }

        public int Subtracao(int v1, int v2)
        {
            return v1 - v2;
        }

        public int Multiplicacao(int v1, int v2)
        {
            return v1 * v2;
        }

        public int Divisao(int v1, int v2)
        {
            return v1 / v2;
        }
    }
}
